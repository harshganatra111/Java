//Odd and Even

import java.lang.*;

class OddEven
{
    public static void main(String[] obj)
    {
	int a,b;
	a=5;
	b=6;
	if(a%2==0)
	System.out.println(a+"is even");
	if(a%2!=0)
	System.out.println(a+"is odd");
	if(b%2==0)
	System.out.println(b+"is even");
	if(b%2!=0)
	System.out.println(b+"is odd");
	
    }
}