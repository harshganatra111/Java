//increment and decrement operators, minus operator (unary operators)

import java.lang.*;

class IncDecOpt1
{
  public static void main(String harsh[])
  {
    int a=7;
    System.out.println(a);
    System.out.println(a++);
    System.out.println(a);
    System.out.println(++a);
    System.out.println(a);
    System.out.println(a--);
    System.out.println(a);
    System.out.println(--a);
    System.out.println(a);
    System.out.println();
    System.out.println("-a="+(-a));
  }
}